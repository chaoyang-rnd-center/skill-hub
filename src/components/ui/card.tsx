import * as React from "react"

import { cn } from "@/lib/utils"

/* Calcite Design System Card Style - Clean, subtle, focused */
function Card({
  className,
  ...props
}: React.ComponentProps<"div">) {
  return (
    <div
      data-slot="card"
      className={cn(
        "flex flex-col rounded bg-white border border-[hsl(210,16%,90%)] shadow-sm",
        "hover:border-[hsl(210,16%,80%)] hover:shadow-md",
        "transition-all duration-150 ease-in-out",
        className
      )}
      {...props}
    />
  )
}

